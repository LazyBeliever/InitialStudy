package DifferenceOfVariable;

public class Domo {

    /**
     * 成员变量
     * 位置：类中，方法外
     * 初始化值：有默认值，无需初始化
     * 内存位置：堆内存（因为对象在堆里
     * 生命周期：随着对象出现和消失
     * 作用域：难以界定
     */


    /**
     * 局部变量
     *位置：常见于方法外
     *初始化值：无默认值，使用前需赋值
     * 内存位置：栈内存
     * 生命周期：随着方法调用及运行结束而存在和消失
     * 作用域：在所归属的大括号中
     */
}

